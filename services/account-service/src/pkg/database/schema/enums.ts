import { text } from "drizzle-orm/sqlite-core";

// Enums
/**
 * Enum representing the types of profiles in the system.
 *
 * @property PROFILE_TYPE_UNSPECIFIED - Default value, used when the profile type is not specified.
 * @property PROFILE_TYPE_USER - Represents a regular user profile.
 * @property PROFILE_TYPE_BUSINESS - Represents a business profile.
 */
export const profileTypeEnum = text("profile_type", [
  "PROFILE_TYPE_UNSPECIFIED",
  "PROFILE_TYPE_USER",
  "PROFILE_TYPE_BUSINESS",
]);

/**
 * Enum representing the different role types in the system.
 *
 * @property ROLE_TYPE_UNSPECIFIED - Default value, used when the role type is not specified.
 * @property ROLE_TYPE_SUPER_ADMIN - Represents a super administrator role with highest privileges.
 * @property ROLE_TYPE_TEAM_ADMIN - Represents a team administrator role with team-level privileges.
 * @property ROLE_TYPE_REGULAR - Represents a regular user role with standard privileges.
 */
export const roleTypeEnum = text("role_type", [
  "ROLE_TYPE_UNSPECIFIED",
  "ROLE_TYPE_SUPER_ADMIN",
  "ROLE_TYPE_TEAM_ADMIN",
  "ROLE_TYPE_REGULAR",
]);

/**
 * Enum representing the types of audit actions in the system.
 *
 * @property AUDIT_ACTION_UNSPECIFIED - Default value, used when the audit action is not specified.
 * @property AUDIT_ACTION_CREATED - Represents a creation action in the audit log.
 * @property AUDIT_ACTION_UPDATED - Represents an update action in the audit log.
 * @property AUDIT_ACTION_DELETED - Represents a deletion action in the audit log.
 */
export const auditActionEnum = text("audit_action", [
  "AUDIT_ACTION_UNSPECIFIED",
  "AUDIT_ACTION_CREATED",
  "AUDIT_ACTION_UPDATED",
  "AUDIT_ACTION_DELETED",
]);

/**
 * Enum representing the possible statuses of an API key.
 *
 * @property ACTIVE - Indicates that the API key is currently active and can be used.
 * @property INACTIVE - Indicates that the API key is temporarily inactive but can be reactivated.
 * @property REVOKED - Indicates that the API key has been permanently revoked and cannot be used.
 */
export const apiKeyStatusEnum = text("api_key_status", [
  "ACTIVE",
  "INACTIVE",
  "REVOKED",
]);
