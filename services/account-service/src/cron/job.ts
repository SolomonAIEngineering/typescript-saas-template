/**
 * Represents a cron job configuration.
 */
export interface CronJob {
  /**
   * The name of the cron job.
   * This should be a unique identifier for the job.
   */
  name: string;

  /**
   * A brief description of what the cron job does.
   * This can be used for documentation or logging purposes.
   */
  description: string;

  /**
   * The function that will be executed when the cron job runs.
   * This should be an asynchronous function that returns a Promise.
   *
   * @returns A Promise that resolves when the job is complete.
   */
  handler: () => Promise<void>;

  /**
   * Indicates whether this cron job requires access to the Cloudflare API.
   * Set to true if the job needs to interact with Cloudflare services.
   */
  requiresCloudflareAPI: boolean;
}
