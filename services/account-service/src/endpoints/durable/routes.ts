import { createRoute, z } from "@hono/zod-openapi";
import { openApiErrorResponses } from "../../pkg/errors/index.js";

export const serviceDurableObjectRoute = createRoute({
  tags: ["serviceDurableObject"],
  operationId: "v1.serviceDurableObject",
  method: "get",
  path: "/v1/service/{slug}/{pathname}",
  request: {
    params: z.object({
      slug: z.string(),
      pathname: z.string(),
    }),
  },
  responses: {
    200: {
      description: "Successful response from the Service Durable Object",
      content: {
        "application/json": {
          schema: z.any(),
        },
      },
    },
    ...openApiErrorResponses,
  },
});
